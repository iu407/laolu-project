public class Pojo implements Serializable{
	private static final long serialVersionUID = -9468311904307660L;
	
	private String name;
	private String age;
	
	
	public Pojo() {
		super();
	}
	
	public Pojo(String name, String age) {
		super();
		this.name = name;
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}

}
